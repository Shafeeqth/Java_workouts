public class Literals {
    public static void main(String[] args) {
        int num1 = 0b101;
        int num2 = 0x5E;
        System.out.println(num1);
        System.out.println(num2);
        long lg = 10_00_000;
        char ch = 'c';
        System.out.println(++ch);
        System.out.println(lg );
        System.out.println(num1);
    }
    
}
